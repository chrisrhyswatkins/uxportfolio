				<p class="portfolio-body">After using Lyft Mirror to search for a ride closely matching the preferences selected, the system will search the local surroundings to find the driver most closely matching those preferences. Naturally, this may not be the closest rider available, so if in a rush, the user may at this time switch to instead search for a close driver, or instead move to the next match. The user is given 60 seconds to confirm the match, after which the request will be cancelled. It is worth noting at this time, that the star rating applied to drivers has been replaced with a level-up concept. A positive ride will award the driver 1 point, while a negative ride will take away 3 points, with more points being required by the driver to progress up each level. This positive or negative rating concept will become clearer in the next section.</p>

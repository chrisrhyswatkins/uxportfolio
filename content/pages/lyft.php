		<div class="row">
			<div class="small-24 medium-22 large-18 columns small-centered fadeInPortfolioText">
				<h2 class="sub-section">Leaving a Negative Rating</h2>
				<p class="portfolio-body">The process of leaving a negative rating for a ride works much in the same way as when leaving a positive rating, but with some key differences. Firstly, the list of potentially negative aspects of the ride to select are requested from the user first, to allow for the recognition that the company is prioritizing their emotions and understanding what made their experience negative. When proceeding to the potentially positive aspects of the ride, the system is dynamic in the sense that the options displayed in this second stage depend greatly on those selected in the first. For example, if the user selects ‘dirty vehicle’ in the negative section, the option of the vehicle being ‘squeaky clean’ will obviously not be available to define as a positive aspect.</p>
				<p class="portfolio-body">The user is also able to give additional comments in the final stage of the rating process, and in order to address the issue where riders would be afraid to give negative ratings out of the fear of matching with that driver again in the future, the user now has the option to ‘block’ a driver, meaning they cannot match with that driver again. The user would of course be able to unblock any drivers within their settings.</p>

			<div class="small-24 medium-22 large-18 columns small-centered fadeInPortfolioText">
				<h2 class="sub-section">Leaving a Positive Rating</h2>
				<p class="portfolio-body">Upon the completion of a ride, the user is no longer asked to provide a potentially inaccurate, and unrepresentative verdict of their experience. Instead they now simply have to match their emotion to the vastly differing visual options available, becoming more of a recognized visceral reaction rather than a recalled calculative effort. If positive, the user proceeds to recognizing and selecting the positive aspects of their ride (the selections in both positive and negative sections help to build up the profile of the driver, influencing the match system). It’s worth noting that if a driver matched based on a preference being over a certain level in their own match system, say 75%, then that preference will be initially selected, but can be deselected by the user.</p>
				<p class="portfolio-body">Even if the ride was deemed positive by the user, it doesn’t mean that everything is perfect, and every piece of feedback is valuable to both the driver for performance improvement, and Lyft as a company. This means that users can now acknowledge any negative aspects of the ride, but without putting the driver at risk of deactivation through a less than perfect rating. The user is also able to add their own critique, if not available in the predefined list.</p>

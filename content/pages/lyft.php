		<div class="row">
			<div class="small-24 medium-22 large-18 columns small-centered fadeInPortfolioText">
				<p class="portfolio-body nothing-below">The trends and patterns presented in the affinity diagram allowed us to identify the dominant problems caused by the current system, that would outline the focus of our succeeding research and design process.</p>
				<p class="capitalized-title-desc above-list-items">Problems</p>
				<ol class="regular">
					<li><p>A "good ride" is completely subjective, with most users only rating 1 or 5 stars, causing innacurate ratings for a variety of reasons.</p></li>
		      		<li><p>The current system places too much pressure on drivers. Anything below a 5 star rating can severely damage a driver's rating.</p></li>
		          	<li><p>Many users are not currently compelled to rate their drivers, deeming it to have little or no effect on their future experience.</p></li>
		          	<li><p>The star system does not allow users to quickly and accurately recall and distinguish their ride quality through a rating.</p></li>
		   		</ol>
